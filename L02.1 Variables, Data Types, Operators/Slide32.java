
public class Slide32 {
	public static void main(String[] args) {
		double result;
		result = 3/2; 
    	System.out.println("result of 3/2 = "+result);
		result = (double)3/2;
    	System.out.println("result of (double)3/2 = "+result);
		result = 3/(double)2;
    	System.out.println("result of 3/(double)2 = "+result);
		result = (double)(3/2);
    	System.out.println("result of (double)(3/2) = "+result);
	}
}