
public class Slide29 {
	public static void main(String[] args) {
		double result;
		result = 3/2; //integer division
    	System.out.println("result of 3/2 = "+result);
		result = 3.0/2.0;
    	System.out.println("result of 3.0/2.0 = "+result);
		result = 3/2.0;
    	System.out.println("result of 3/2.0 = "+result);
		result = 3.0/2;
    	System.out.println("result of 3.0/2 = "+result);
	}
}